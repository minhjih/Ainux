_DEFAULT_ASSETS: Final = {
    "ainux.png": DEFAULT_AINUX_LOGO_BASE64,
    "ainux_penguin.png": DEFAULT_AINUX_PENGUIN_BASE64,
}


@lru_cache(maxsize=4)
def _load_bytes(name: str) -> bytes:
    """Load raw bytes for an asset, preferring disk overrides."""

    disk_candidate = _BRANDING_ROOT / name
    if disk_candidate.exists():
        return disk_candidate.read_bytes()

    try:
        encoded = _DEFAULT_ASSETS[name]
    except KeyError as exc:
        raise FileNotFoundError(f"Unknown Ainux asset: {name}") from exc

    return base64.b64decode(encoded)


@lru_cache(maxsize=4)
def _as_data_uri(name: str) -> str:
    data = base64.b64encode(_load_bytes(name)).decode("ascii")
    return f"data:image/png;base64,{data}"


